import React, { useContext } from "react";
import { GlobalContext } from "../context/GlobalState";
import { useAuth } from "../context/AuthContext"; 

export const MovieControls = ({ type, movie }) => {
  const { currentUser } = useAuth(); // Get the currentUser from your AuthContext
  const {
    removeMovieFromWatchlist,
    addMovieToWatched,
    moveToWatchlist,
    removeFromWatched,
  } = useContext(GlobalContext);

  return (
    <div className="inner-card-controls">
      {currentUser && type === "watchlist" && ( // Check if currentUser is available and type is watchlist
        <>
          <button className="ctrl-btn" onClick={() => addMovieToWatched(movie)}>
            <i className="fa-fw far fa-eye"></i>
          </button>

          <button
            className="ctrl-btn"
            onClick={() => removeMovieFromWatchlist(movie.id)}
          >
            <i className="fa-fw fa fa-times"></i>
          </button>
        </>
      )}

      {currentUser && type === "watched" && ( // Check if currentUser is available and type is watched
        <>
          <button className="ctrl-btn" onClick={() => moveToWatchlist(movie)}>
            <i className="fa-fw far fa-eye-slash"></i>
          </button>

          <button
            className="ctrl-btn"
            onClick={() => removeFromWatched(movie.id)}
          >
            <i className="fa-fw fa fa-times"></i>
          </button>
        </>
      )}
    </div>
  );
};
